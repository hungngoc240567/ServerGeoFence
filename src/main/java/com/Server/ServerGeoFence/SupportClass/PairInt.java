package com.Server.ServerGeoFence.SupportClass;

public class PairInt {
    private final int first;
    private final int second;

    public PairInt(int first, int second) {
        this.first = first;
        this.second = second;
    }

    public int getFirst() {
        return first;
    }

    public int getSecond() {
        return second;
    }
}
